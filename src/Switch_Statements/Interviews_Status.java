package Switch_Statements;

public class Interviews_Status {
public static void main(String[] args) {
	int Status=4;
	
	switch (Status) {
	case 1: {
		System.out.println("Interview done successfulyy");
	}
	case 2: {
		System.out.println("Interview got failed");
	}
	case 3: {
		System.out.println("Status as rejected");
	}
	default:
	{
		System.out.println("done with interview");
	}
	}
}
}
